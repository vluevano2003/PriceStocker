-- ==========================================
-- 1. TABLAS INDEPENDIENTES (CATÁLOGOS)
-- ==========================================

CREATE TABLE usuario (
    idusuario INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombreusuario VARCHAR(50) NOT NULL UNIQUE,
    contrasenausuario VARCHAR(255) NOT NULL,
    permiso BOOLEAN DEFAULT FALSE
);

CREATE TABLE categoria (
    idcategoria INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombrecategoria VARCHAR(100) NOT NULL UNIQUE,
    desccategoria TEXT,
    -- Nota: En tu dump tenías columnas duplicadas (id_categoria vs idcategoria). 
    -- He unificado todo en idcategoria para el estándar Java.
    descripcion_categoria VARCHAR(255)
);

CREATE TABLE ruta (
    idruta INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    salidaruta VARCHAR(100) NOT NULL,
    destinoruta VARCHAR(100) NOT NULL
);

